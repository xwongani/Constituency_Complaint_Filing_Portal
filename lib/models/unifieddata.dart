class UnifiedData {
  final String fullname;
  final String email;
  final String phone;
  final String complaint;
  final String details;
  final String status;
  final String priority;

  UnifiedData({
    required this.fullname,
    required this.email,
    required this.phone,
    required this.complaint,
    required this.details,
    required this.status,
    required this.priority,
  });
}
